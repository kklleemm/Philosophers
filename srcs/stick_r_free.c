#include <philo.h>

int					g_stick_r_free(t_philo *p)
{
	if ((p->id == 6 && g_sticks[0] != S_FREE)
			|| (p->id != 6 && g_sticks[p->id + 1] != S_FREE))
		return (0);
	return (1);
}
